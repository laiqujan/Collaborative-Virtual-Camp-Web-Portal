For tenants:

How it works?
1. How does this work?
This website is designed for tenants to find full units (either HDB or condo) without paying any agent fee. 
Tenants should first submit a request with all details such as preferred location, budget etc. 
Agents would then respond to the requests if they have any available properties, with details such as location, asking price (whether negotiable), contract term etc. 
Tenants can choose to accept any of the responses; afterwhich your contact details would be shared with the agents. 
Tenants and agents can proceed to contact each other directly to view the unit. 

2. If i am not paying, how are the agents paid?
The agents represent the landlords and are paid for by the landlords themselves. 

3. Does the agent represent both landlord and the tenant?
No, the agent only represents the landlord. 

4. Can I request for a room rental?
No, currently we only support full units rentals. We might support room rental in the future, though. 

5. Can I use your services for purhase of hdb/condo?
No, currently we only support full units rentals. We will support sales of HDB/Condo in the future, though. 


Fees
6. Is there any fees?
No, there are no fees whatsover. We do not collect any fees, nor should the agent. 
If the agent attempts to collect any commission or fees other than legally payable charges such as stamp duty, notify us immediately via email. 


Disputes
7. Agent tells that the property that he proposed has been taken and there are other properties which involve a commission. What should you do?
Such cases are likely cases of fake listing and we take fake listings very seriously. Please report him via email to "support@blk1sg.com". We will take necessary action.

8. If I have any dispute with the agent or the landlord after I begin my tenancy, how should I resolve?
If you have any complaint against the agent, you can consider filing a complaint with the CEA. For disputes with the landlord, if you are unable to solve it yourself, 
you may consider involving police and for financial matter, small claims tribunal. 



For agents:

1. Who can register as an agent?
Agent must be registered agent in Singapore (registered with CEA).

2. What are my responsbilities as an agent?
You should only respond if you have a property in which you represent the owner and the commission is paid by the owner. 
The tenants from this platform doesn't pay any agent fee whatsover. 
You should only list the properties you have. We take fake listings very seriously and we might suspend or ban accounts which submit fake responses. 

3. Can I list non-hdb approved units?
No. you may not. All HDB units should be approved by HDB for rental before you post them here. 

4. How do I upload pictures/video of house to this website?
We currently does not allow this since we assume that you might have already listed this unit elsewhere (other property portals).
Please feel free to post the url of the property listing here. 

5. Is there any fee for using this service?
No, we dont charge any fee from either tenants or agents. 

6. Why is my CEA registration number required?
We only allow CEA registered agents to be registered on this website.

7. My account is suspended/terminated. Why?
We might suspend/terminate accounts which don't follow our terms of use (usually for posting fake responses to tenancy request). 
If you feel that we have suspended your account unfairly, please contact us via email: "support@blk1sg.com"

General
1. If you dont charge either tenants or agents, how do you make money?
We dont. 

2. Whom should I contact for business enquiry (partnership/advertising/merger/acquisition etc)
Please email "contact@blk1sg.com"

3. Can I acquire blk1sg.com for one million dollars?
Probably. Please email "contact@blk1sg.com"

3. I have a query not answered here. What should I do?
Please email "contact@blk1sg.com"
